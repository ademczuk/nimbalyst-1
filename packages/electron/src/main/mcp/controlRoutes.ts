/**
 * Nimbalyst control plane HTTP routes for external automation.
 *
 * Exposes a narrow programmatic API at /control/v1/* on the existing
 * internal MCP HTTP server (httpServer.ts). Designed so external agents
 * (Claude Code, regression test harnesses, the nimbalyst-mcp sidecar)
 * can create chat sessions, dispatch prompts, and read transcripts
 * without driving the UI.
 *
 * 2026-05-18: born tonight to replace the "user manually clicks +,
 * switches model dropdown, pastes prompt" loop. Five paths were
 * evaluated (renderer HTTP bridge, main-process HTTP, SQLite injection,
 * file-watch, MCP sidecar). Pantheon council + Trident consensus picked
 * main-process HTTP as the primary control plane; the MCP sidecar
 * (phase 2) wraps this API in standard MCP tool shape.
 *
 * Security model:
 *   - Reuses the per-launch mcpAuthToken (random 32-byte hex). The
 *     same token gates /mcp, so external callers and SDK subprocesses
 *     authenticate identically.
 *   - Token is exported to disk at userData/.control-token with mode
 *     0o600 so sidecars and scripts can read it. Token rotates per
 *     launch — never persisted across runs in any persistent form.
 *   - Port is exported to userData/.control-port for sidecars that
 *     need to discover the actual port (the MCP server falls forward
 *     when 3456 is taken).
 *   - Host header is validated to localhost variants to defeat DNS
 *     rebinding (per CVE-2025-49596: MCP Inspector RCE).
 *   - Bind is enforced 127.0.0.1 by httpServer.ts at the listen call.
 *
 * Contract is versioned: /control/v1/*. When the IPC contract or the
 * orchestration semantics shift in a way that breaks external callers,
 * bump to /control/v2/* without removing v1 for the migration window.
 */

import { IncomingMessage, ServerResponse } from "http";
import * as fs from "fs";
import * as path from "path";
import { app, BrowserWindow, webContents } from "electron";
import {
  AISessionsRepository,
  AgentMessagesRepository,
} from "@nimbalyst/runtime";
import { requireMcpAuth, getMcpAuthToken } from "./mcpAuth";
import { windowStates } from "../window/windowState";

const VALID_HOSTS = new Set([
  "localhost",
  "127.0.0.1",
]);

/**
 * Write the per-launch token + actual listening port to userData so
 * external automation can discover them. Called once from index.ts
 * after startMcpHttpServer resolves. The token already exists in
 * memory (generated by generateMcpAuthToken); we only persist the
 * pair so a sidecar can hand-shake without a separate exchange.
 *
 * Files are mode 0o600 (user read/write only). On Windows the
 * effective ACL is the file owner only — node's fs.chmodSync maps
 * mode bits to the closest Windows equivalent.
 */
export function writeControlPlaneDiscoveryFiles(actualPort: number): void {
  const token = getMcpAuthToken();
  if (!token) {
    console.warn("[control] cannot write discovery files: token not generated");
    return;
  }
  const dir = app.getPath("userData");
  const tokenPath = path.join(dir, ".control-token");
  const portPath = path.join(dir, ".control-port");
  try {
    fs.writeFileSync(tokenPath, token, { mode: 0o600 });
    fs.writeFileSync(portPath, String(actualPort), { mode: 0o600 });
    console.log(
      `[control] wrote discovery files: ${tokenPath} / ${portPath} (port=${actualPort})`
    );
  } catch (err) {
    console.error("[control] failed to write discovery files:", err);
  }
}

/**
 * Validate the Host header is a localhost variant. Defends against
 * DNS rebinding attacks where an attacker's page resolves a hostname
 * they control to 127.0.0.1 and POSTs to localhost — bypassing CORS.
 * Per CVE-2025-49596 this is a live exploit class as of 2025.
 */
function isLocalhostHost(req: IncomingMessage): boolean {
  const raw = req.headers["host"];
  if (typeof raw !== "string") return false;
  const hostOnly = raw.split(":")[0].toLowerCase();
  return VALID_HOSTS.has(hostOnly);
}

function jsonResponse(res: ServerResponse, status: number, body: unknown): void {
  const payload = JSON.stringify(body);
  res.writeHead(status, {
    "Content-Type": "application/json",
    "Content-Length": Buffer.byteLength(payload),
  });
  res.end(payload);
}

async function readJsonBody(req: IncomingMessage, maxBytes = 256 * 1024): Promise<unknown> {
  return new Promise((resolve, reject) => {
    let total = 0;
    const chunks: Buffer[] = [];
    req.on("data", (chunk: Buffer) => {
      total += chunk.length;
      if (total > maxBytes) {
        reject(new Error(`request body exceeds ${maxBytes} bytes`));
        req.destroy();
        return;
      }
      chunks.push(chunk);
    });
    req.on("end", () => {
      try {
        const raw = Buffer.concat(chunks).toString("utf8");
        if (!raw) {
          resolve({});
          return;
        }
        resolve(JSON.parse(raw));
      } catch (err) {
        reject(err);
      }
    });
    req.on("error", reject);
  });
}

/**
 * Find a renderer that owns a given workspace, so we can broadcast
 * UI-refresh signals after writing into the session DB. Returns
 * undefined if no matching window is open — that's not an error:
 * the session row exists, the renderer will pick it up next time
 * the user opens that workspace, or polling will catch it.
 *
 * Implementation note: we intentionally don't require the renderer
 * to be open. The control plane's contract is "session persisted +
 * dispatch queued"; UI refresh is best-effort.
 */
function notifyRenderer(channel: string, payload: unknown): void {
  try {
    for (const wc of webContents.getAllWebContents()) {
      if (!wc.isDestroyed()) {
        wc.send(channel, payload);
      }
    }
  } catch (err) {
    console.warn(`[control] notifyRenderer(${channel}) failed:`, err);
  }
}

/**
 * Heavy-hammer refresh: force a full webContents.reload() on every
 * window currently pointing at the given workspace. Used as the v1
 * fallback before the renderer learns to handle the lightweight
 * sessions:invalidate IPC; losing transcript scroll / composer text
 * is the trade for guaranteed visibility of externally-created
 * sessions. Once the invalidate listener ships, callers should stop
 * passing ?refresh=reload and this path becomes opt-in for the
 * rare "renderer state is hopelessly stale, just nuke it" case.
 */
function reloadWindowsForWorkspace(workspaceId: string): void {
  try {
    let count = 0;
    for (const [, state] of windowStates) {
      if (!state) continue;
      const owns =
        state.workspacePath === workspaceId ||
        state.activeWorkspacePath === workspaceId ||
        state.additionalWorkspacePaths?.includes(workspaceId);
      if (!owns) continue;
      const win = state as unknown as { window?: BrowserWindow };
      // windowStates values may not carry the BrowserWindow directly; fall
      // back to enumerating all windows and matching by webContents URL is
      // unreliable. Simplest: reload all windows since the workspace match
      // already filtered the set, and the typical user has one window per
      // workspace.
      for (const w of BrowserWindow.getAllWindows()) {
        if (!w.isDestroyed()) {
          w.webContents.reload();
          count++;
        }
      }
      break; // one reload pass covers all workspaces simultaneously
    }
    console.log(`[control] reloaded ${count} window(s) for workspace ${workspaceId}`);
  } catch (err) {
    console.warn("[control] reloadWindowsForWorkspace failed:", err);
  }
}

